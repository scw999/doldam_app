function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function extractRegion(theme: string): string | null {
  for (const region of Object.keys(REGION_QUESTIONS)) {
    if (theme.includes(region)) return region;
  }
  return null;
}

export function buildWelcomeMessage(ctx: RoomContext): string {
  const { theme, genderMix } = ctx;
  const genderLabel =
    genderMix === 'mixed' ? '남녀 돌싱' :
    genderMix === 'men_only' ? '남성 돌싱' : '여성 돌싱';

  // "서울 강남구 남녀 모임" → "서울 강남구"
  const regionPart = theme
    .replace('남녀 모임', '')
    .replace('남성 모임', '')
    .replace('여성 모임', '')
    .trim();

  const location = regionPart && regionPart !== '전국' ? `${regionPart} 지역의 ` : '';

  return `안녕하세요! 돌담이 ${location}${genderLabel} 여러분을 연결해드렸어요 🎉\n편하게 자기소개 부터 해볼까요?`;
}

export function buildIcebreakerQuestion(ctx: RoomContext): string {
  const tags = ctx.tags ? ctx.tags.split(',').map((t) => t.trim()).filter(Boolean) : [];
  const region = extractRegion(ctx.theme);

  // 1순위: 관심사 기반
  for (const tag of tags) {
    const pool = INTEREST_QUESTIONS[tag];
    if (pool) return pickRandom(pool);
  }

  // 2순위: 지역 기반
  if (region) {
    return pickRandom(REGION_QUESTIONS[region]);
  }

  // 3순위: 성별 기반
  const genderPool = GENDER_QUESTIONS[ctx.genderMix];
  if (genderPool && Math.random() > 0.4) {
    return pickRandom(genderPool);
  }

  // 4순위: 일반
  return pickRandom(GENERAL_QUESTIONS);
}
